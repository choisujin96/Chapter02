package com.javaex.ex04;

public class Goods {//틀, 도장

	
	//필드
	private String name;
	private int price;
	
	
	//생성자
	
	
	
	//메소드
	
	
	//name등록
	public void setName(String n) {
		name = n;
	}
	

	//name읽어오기
	public String getName() {
		return name;
	}
	
	
	//price등록
	public void setPrice(int p) {
		price=p;
	}
	
	
	
	//price읽어오기
	public int getPrice() {
		return price;
	}
	
}
